CREATE TABLE SOLICITACOES (
    ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SISTEMA VARCHAR(100),
    TIPO_SOLICITACAO VARCHAR(100),
    NOME_SOLICITANTE VARCHAR(100),
    TELEFONE VARCHAR(20),
    EMAIL VARCHAR(100),
    DESCRICAO BLOB SUB_TYPE TEXT,
    CAMINHO_ANEXO VARCHAR(255),
    DTHR_CRIACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
